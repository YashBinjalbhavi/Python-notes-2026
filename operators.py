# Arithmetic Operators

a = 10
b = 3

print(a + b)   # 13  → adds values
print(a - b)   # 7   → subtracts
print(a * b)   # 30  → multiplies
print(a / b)   # 3.33 → divides (gives float)
print(a % b)   # 1   → remainder
print(a ** b)  # 1000 → power (10³)


# Relational / Comparison Operators

x = 5
y = 10

print(x == y)  # False → values not same
print(x != y)  # True  → values are different
print(x > y)   # False → 5 is not greater
print(x < y)   # True  → 5 is smaller
print(x >= 5)  # True  → equal is allowed
print(y <= 9)  # False → 10 is bigger


# Assignment Operators
# Example 1
a = 10
a += 5          # a = 10 + 5
print(a)        # 15

# Example 2
a = 10
a *= 5          # a = 10 * 5
print(a)        # 50


# Example 3
a = 10
a -= 5          # a = 10 - 5
print(a)        # 5


# Example 4
a = 10
a /= 5          # a = 10 / 5
print(a)        # 2.0
